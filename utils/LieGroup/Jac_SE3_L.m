function J = Jac_SE3_L(xi)
x = [skew(xi(1:3)), skew(xi(4:6));...
    zeros(3), skew(xi(1:3))];
t = norm(xi(1:3));
J = eye(6) + (4 - t * sin(t) - 4 * cos(t)) / 2 / t^2 * x + ...
    (4 * t - 5 * sin(t) + t * cos(t)) / 2 / t^3 * x^2 + ...
    (2 - t * sin(t) - 2 * cos(t)) / 2 / t^4 * x^3 + ...
    (2 * t - 3 * sin(t) + t * cos(t)) / t^5 * x^4;
J(4:6,1:3) = J(1:3,4:6);
J(1:3,4:6) = zeros(3);
end